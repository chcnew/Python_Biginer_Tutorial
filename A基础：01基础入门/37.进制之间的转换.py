# 十进制转为二进制 bin()
# 十进制转为八进制 oct()
# 十进制转为十六进制 hex()
# 十进制转换为其他进制都以字符串存在
print(bin(25), type(bin(25)))  # 0b11001 <class 'str'>
print(oct(25), type(oct(25)))  # 0o31 <class 'str'>
print(hex(25), type(hex(25)))  # 0x19 <class 'str'>
