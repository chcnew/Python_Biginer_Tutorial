# 舍入函数
import math

# 大于x的最近整数
print(math.ceil(1.4))
# 小于x的最近整数
print(math.floor(1.4))
# 四舍五入函数
print(round(1.5))
# 对数运算
print(math.log(16, 4))  # 求4为底，16的对数
# 平方根运算
print(math.sqrt(4))  # 求4的平方根
# 幂运算
print(math.pow(4, 2))  # 求4的2次幂
# 弧度x的三角正弦
print(math.sin(30))
# 弧度x的三角余弦
print(math.cos(30))
# 弧度x的三角正切
print(math.tan(45))

# 弧度x的三角反正弦
print(math.asin(1))
# 弧度x的三角反余弦
print(math.acos(1))
# 弧度x的三角反正切
print(math.atan(45))
